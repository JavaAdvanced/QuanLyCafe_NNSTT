    public void showTT(int i) {
        jPanel2.removeAll();
        switch(i) {
            case 1: 
                if(HH ==  null)
                    HH = new ThongKeHanghoa();
                jPanel2.add(HH);
                break;
            case 2:
                if(HD == null)
                    HD = new ThongKeHoaDon();
                jPanel2.add(HD);
                break;       
        }
        jPanel2.updateUI();
    }
    
    
